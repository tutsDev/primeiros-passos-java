/*
Escreva um algoritmo que imprima a sequência do número 100 ao 200;
*/

public class ImprimeNumerosDo100ao200 {
    
    public static void main(String[] args) {
        
        int contador = 100;

        while (contador <= 200) {
            System.out.println(contador);
            contador++;
        }

    }

}
